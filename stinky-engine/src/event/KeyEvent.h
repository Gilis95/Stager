#pragma once

#include "event/Event.h"
#include "core/KeyboardCodes.h"

namespace stinky {

    /////////////////////////////////////////////////////////////////////////////////////////
    /////////////////////////////////////////////////////////////////////////////////////////
    class KeyPressedEvent : public Event {
    public:
        explicit KeyPressedEvent(KeyCode key) : EVENT_CONSTRUCTOR(KeyPressed), m_Key(key) {}

        KeyCode m_Key;
    };

    /////////////////////////////////////////////////////////////////////////////////////////
    /////////////////////////////////////////////////////////////////////////////////////////
    class KeyReleasedEvent : public Event {
    public:
        explicit KeyReleasedEvent(KeyCode key) : EVENT_CONSTRUCTOR(KeyReleased), m_Key(key) {}

        KeyCode m_Key;
    };

    /////////////////////////////////////////////////////////////////////////////////////////
    /////////////////////////////////////////////////////////////////////////////////////////
    class KeyTypedEvent : public Event {
    public:
        explicit KeyTypedEvent(KeyCode key) : EVENT_CONSTRUCTOR(KeyTyped), m_Key(key) {}

        KeyCode m_Key;
    };
}